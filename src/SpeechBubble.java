public class SpeechBubble {

    public boolean isTalking;
    public int xpos;
    public int ypos;
    public int width;
    public int height;

    public SpeechBubble(boolean pIsTalking,int pXpos, int pYpos, int pWidth,int pHeight) {

        isTalking = pIsTalking;
        xpos = pXpos;
        ypos = pYpos;
        width = pWidth;
        height = pHeight;
    }
}
